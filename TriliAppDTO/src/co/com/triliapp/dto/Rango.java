package co.com.triliapp.dto;

/**
 *
 * @author Estudiante 2018
 */
public class Rango {

    private Integer idRango;
    private String nombreRNG;
    private String imagenRNG;
    private String descripcionRNG;

    public Integer getIdRango() {
        return idRango;
    }

    public void setIdRango(Integer idRango) {
        this.idRango = idRango;
    }

    public String getNombreRNG() {
        return nombreRNG;
    }

    public void setNombreRango(String nombreRNG) {
        this.nombreRNG = nombreRNG;
    }

    public String getImagenRNG() {
        return imagenRNG;
    }

    public void setImagenRNG(String imagenRNG) {
        this.imagenRNG = imagenRNG;
    }

    public String getDescripcionRNG() {
        return descripcionRNG;
    }

    public void setDescripcionRNG(String descripcionRNG) {
        this.descripcionRNG = descripcionRNG;
    }

}
